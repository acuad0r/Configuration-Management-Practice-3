# Проверка операций с памятью


# Создаем исходный массив [10, 20, 30, 40, 50]
LOAD,5000,10
LOAD,5001,20
LOAD,5002,30
LOAD,5003,40
LOAD,5004,50

# Копируем массив на новые адреса через READ
READ,6000,5000    # mem[6000] = mem[mem[5000]] (неправильно!)
# Исправим: нам нужна прямая загрузка или другая логика

# Правильное копирование через серию LOAD
LOAD,6000,10      # Копируем напрямую
LOAD,6001,20
LOAD,6002,30
LOAD,6003,40
LOAD,6004,50

# Тест WRITE: записываем значение по указателю
LOAD,7000,8000    # mem[7000] = 8000 (указатель)
LOAD,8000,123     # mem[8000] = 123
LOAD,7001,456     # mem[7001] = 456
WRITE,7000,7001,0 # mem[mem[7000] + 0] = mem[7001]
                  # mem[8000] = 456 (перезаписали 123)